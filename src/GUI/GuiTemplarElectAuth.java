    private void jTabbedPane1StateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_jTabbedPane1StateChanged
        if (jTabbedPane1.getSelectedComponent() == UsersList) {
            DefaultListModel model = new DefaultListModel();
            model.addAll(User.getUserList());
            jListUsers.setModel(model);
        }
    }//GEN-LAST:event_jTabbedPane1StateChanged

    private void jListUsersValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_jListUsersValueChanged
        if (jListUsers.getSelectedIndex() >= 0) {
            User u = (User) jListUsers.getSelectedValues()[0];
            txtUser.setText(u.getName());
            txtPass.requestFocus();
            jTabbedPane1.setSelectedComponent(PanelLogin);
        }
    }//GEN-LAST:event_jListUsersValueChanged

