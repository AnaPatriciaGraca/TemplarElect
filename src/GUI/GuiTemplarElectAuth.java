    private void btRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btRegisterActionPerformed
        try {
            User u = User.load(txtUserRegister.getText());
            JOptionPane.showMessageDialog(this, "User already exists");
            return;
        } catch (Exception ex) {
        }
        try {
            if (!Arrays.equals(txtPassRegister.getPassword(), txtPassRegister2.getPassword())){
                JOptionPane.showMessageDialog(this, "Passwords don't match");
                return;
            }
            User.register(txtUserRegister.getText(), new String(txtPassRegister.getPassword()));
            JOptionPane.showMessageDialog(this, "User Registed");
        } catch (Exception ex) {
            java.util.logging.Logger.getLogger(GuiTemplarElectAuth.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_btRegisterActionPerformed

    private void btLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btLoginActionPerformed
        try {
            User u = User.load(txtUser.getText(), new String(txtPass.getPassword()));
            System.out.println(u.getName());
            JOptionPane.showMessageDialog(this, "Login Success!");
            this.dispose();
            new GuiTemplarElect(u).setVisible(true);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Incorrect User or Password");
        }
    }//GEN-LAST:event_btLoginActionPerformed

